public class ForLoop{
    public static void main(String[] args) {
        System.out.println("Increse");
        for (int i = 0; i <= 10; i++) {
            System.out.println("The value of i is " + i);
        }
        
        System.out.println("Decrese");
        for (int i = 10; i >= 0; i--) {
            System.out.println(i);
        }
    }

        
}